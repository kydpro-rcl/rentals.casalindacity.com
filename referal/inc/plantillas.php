<?php

	function dibujar($page){		require_once(TEMPLATE_HEADER);
		require_once("pages/".$page.".php");
		require_once(TEMPLATE_FOOTER);
	}

	function dibujar_login($page){
		require_once(TEMPLATE_CABEZA);
		require_once("pages/".$page.".php");
		require_once(TEMPLATE_PIE);
	}

	function dibujar_clear($page){
		require_once(TEMPLATE_CAB);
		require_once("pages/".$page.".php");
		require_once(TEMPLATE_PI);
	}

?>