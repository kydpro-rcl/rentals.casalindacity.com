<h2>EL LIMON WATERFALL</h2>
<p>Visit the most beautiful waterfall in the Dominican Republic and enjoy the tropical forest.<br/>
Pick up at reception in the Casa Linda Welcome Center at 6:15 am. <br/>
The bus will follow the north coast towards the peninsula of Samana. Just the drive is an amazing experience with beautiful ocean vistas and tropical jungle views. We will arrive at the small town of "El Cafe" where Ramona and Basilio will be waiting to greet us.  We will begin our adventure with a mule ride. Don’t worry…. (Every mule has a guide).  We will be lead into the tropical forest until we reach “el limon” waterfall, where we can swim at the base of the waterfall.<br/>
Lunch is included and is a typical dominican menu, rice, chicken, beans, salad, pasta and arepita de yuca. <br/>
On the way back we will stop at  Las Terrenas’ beautiful white sand beach and clear crystal water. We can enjoy for about one hour.<br/>
Return to Casa Linda about 7.30 pm.<br/>
Price 89 US / kids 50 US</p>
