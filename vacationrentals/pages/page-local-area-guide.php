<h2>JEEP SAFARI “LOS BRAZOS”</h2>

<p>Pick up at reception in the Casa Linda Welcome Center at 9:30 am.</p>

<p>Join us for a fun filled day where we will travel by jeep, boat and inner tubes.  <br/>
We leave towards the town of Islabon where we will meet the boat that will take us to where the Yasica and Islabon rivers meet and both flow to the sea.</p>

<p>From there we return to the jeep to continue our trip to a private place in the town “Los brazos”, with a wonderful view of the river.  There we will enjoy a 30 minute leisurely inner tube ride.  </p>

<p>Price Includes a typical Dominican lunch:  rice, beans and chicken made over a wood fire.</p>

<p>Return time about 4.30 pm.</p>

<p>Precio 59 US</p>

