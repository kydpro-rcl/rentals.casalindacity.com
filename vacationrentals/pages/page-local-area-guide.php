<h2>CAYO PARAISO</h2>
<p>This is a full day of fun under the sun and in the water.  Snorkeling gear is provided. </p>

<p>Pick up at reception in the Casa Linda Welcome Center at 7:15 am.</p>

<p>The bus will take us to Punta Rucia Beach where we will take the speed boat to Paradise Island. <br/>
Enjoy time on the island for about two and a half hours.<br/>
Snacks on the island will be provided like seasonal fruits, Pineapple, Watermelon, Mango, Guinean, Coconut, and an open bar with coca cola, sprite, cuba libre, mineral water.<br/>
Admission to the Monte Cristi National Park is included with a visit to the mangroves.<br/>
After spending time on the island, enjoy a beachside lunch just 60 meters from the ocean.  Buffet of rice, string beans, chicken, fish, Six Varieties of salads.<br/>
Return to Punta Rusia to board the bus to Casa Linda. <br/>
Return time about 6 pm.</p>

<p>Price 53 US</p>
