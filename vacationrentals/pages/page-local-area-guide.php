<p>A little bit of history visiting the largest city on the north coast.<br/>
Pick up 7:15 a.m at the Welcome Center reception.</p>

<p>We drive to Puerto Plata where we will visit the Brugal rum factory. <br/>
The Malecón or maritime road will take us to Fuerte de San Felipe, the first defensive construction of the city dating from the 16th century.<br/>
We continue with the Amber Museum and and the many gift shops around the Parque Central.<br/>
Retur n to Casa Linda about 1 pm.<br/>
Price 25 US (minimum 4 pax)</p>
