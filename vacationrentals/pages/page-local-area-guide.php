<h2>SANTIAGO SHOPPING</h2>

<p>For you Shopaholics!  Enjoy of full day of shopping in Santiago. This trip includes some of the best shopping malls and first class shops as well as a leisurely lunch. </p>

<p>Pick up at reception in the Casa Linda Welcome Center at 8 am. </p>

<p>First stop at “Colinas Mall”, with about 1 hour and a half time to shop, next stop at Pricesmart about 40 minutes time and we finally a stop at Supermercado Bravo.</p>

<p>Lunch will be at “La Parrillita”. After lunch we will go to Casa Cuesta and Ferreteria Bellon.</p>

<p>Return time about 7 pm.</p>

<p>Price 20 US</p>

